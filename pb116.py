def comb(L, tile_len):
    if L < 2:
        return 0
    try:
        return {(2, 2): 1,
                (3, 2): 2,
                (4, 2): 4,
                (5, 2): 7,
                (6, 2): 12,
                (7, 2): 20,
                (8, 2): 33,
                (9, 2): 54,
                (10, 2): 88,
                (11, 2): 142,
                (12, 2): 232,
                (13, 2): 376,
                (14, 2): 609,
                (15, 2): 986,
                (16, 2): 1596,
                (17, 2): 2583,
                (18, 2): 4180,
                (19, 2): 6764,
                (20, 2): 10945,
                (21, 2): 17710,
                (22, 2): 28656,
                (23, 2): 46367,
                (24, 2): 75024,
                (25, 2): 121392,
                (26, 2): 196417,
                (27, 2): 317810,
                (28, 2): 514228,
                (29, 2): 832039,
               }[L, tile_len]
    except:
        s = 0
        for i in xrange(L-tile_len+1):
            s += comb(L-tile_len-i, tile_len)
            s += 1
    return s
